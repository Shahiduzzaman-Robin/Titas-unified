// Create a reverse mapping for English to Bengali if needed
export const REVERSE_DICTIONARY: Record<string, string> = Object.fromEntries(
    Object.entries(FALLBACK_DICTIONARY).map(([bn, en]) => [en, bn])
);

export const localize = (text: string | null | undefined, locale: string): string => {
    if (!text) return '';
    const isBn = locale === 'bn';
    
    // If it's in the dictionary, we have an explicit mapping
    if (isBn) {
        return REVERSE_DICTIONARY[text] || text;
    } else {
        return FALLBACK_DICTIONARY[text] || text;
    }
};
